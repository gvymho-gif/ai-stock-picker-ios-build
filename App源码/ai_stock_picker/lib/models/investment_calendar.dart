/// A股板块趋势月历数据模型
/// 4×3布局，12个月份，每个格子两个板块
/// 每个板块预设20+股票代码，确保数据充足

class MonthSectorData {
  final int month;
  final String monthName;
  final List<SectorInfo> sectors;

  const MonthSectorData({
    required this.month,
    required this.monthName,
    required this.sectors,
  });
}

class SectorInfo {
  final String name;
  final String displayName;
  final String keywords;
  final List<String> stockCodes;
  final double changePct;

  const SectorInfo({
    required this.name,
    required this.displayName,
    required this.keywords,
    this.stockCodes = const [],
    this.changePct = 0.0,
  });
}

