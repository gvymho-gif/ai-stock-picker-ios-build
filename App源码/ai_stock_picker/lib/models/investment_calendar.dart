  /// 获取当前月份数据
  static MonthSectorData getCurrentMonthData() {
    final currentMonth = DateTime.now().month;
    return calendarData.firstWhere((d) => d.month == currentMonth, orElse: () => calendarData[0]);
  }

  /// 获取指定月份数据
  static MonthSectorData getMonthData(int month) {
    return calendarData.firstWhere((d) => d.month == month, orElse: () => calendarData[0]);
  }
